char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		q;

	i = 0;
	q = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[q] != '\0')
	{
		s1[i] = s2[q];
		i++;
		q++;
	}
	s1[i] = '\0';
	return (s1);
}
