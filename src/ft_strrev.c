#include "libft.h"

char	*ft_strrev(char *s)
{
	int		i;
	int		q;
	char	tmp;

	i = 0;
	q = ft_strlen(s) - 1;
	while (i < q)
	{
		tmp = s[i];
		s[i] = s[q];
		s[q] = tmp;
		i++;
		q--;
	}
	return (s);
}
