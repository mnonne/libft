#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	q;

	i = ft_strlen(s1);
	q = 0;
	while (n > 0 && s2[q] != '\0')
	{
		s1[i++] = s2[q];
		q++;
		n--;
	}
	s1[i] = '\0';
	return (s1);
}
